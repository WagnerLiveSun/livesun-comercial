CREATE TABLE IF NOT EXISTS contas_banco (
    id INT AUTO_INCREMENT PRIMARY KEY,
    empresa_id INT NOT NULL,
    nome VARCHAR(150) NOT NULL,
    banco VARCHAR(50) NOT NULL,
    agencia VARCHAR(10) NOT NULL,
    numero_conta VARCHAR(20) NOT NULL,
    dv VARCHAR(2) NULL,
    tipo VARCHAR(20) NULL,
    fluxo_conta_id INT NULL,
    saldo_inicial DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    is_principal TINYINT(1) NOT NULL DEFAULT 0,
    ativo TINYINT(1) NOT NULL DEFAULT 1,
    criado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    atualizado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_conta_empresa FOREIGN KEY (empresa_id) REFERENCES empresas(id),
    CONSTRAINT fk_conta_fluxo FOREIGN KEY (fluxo_conta_id) REFERENCES fluxo_contas_modelo(id),
    INDEX idx_conta_empresa (empresa_id),
    INDEX idx_conta_nome (nome),
    INDEX idx_conta_principal (is_principal)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

